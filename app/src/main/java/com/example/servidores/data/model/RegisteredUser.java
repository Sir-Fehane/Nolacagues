package com.example.servidores.data.model;

public class RegisteredUser {

    private String name;
    private String lastname;
    private String email;
    private int id;

    public String getName() {
        return name;
    }

    public String getLastname() {
        return lastname;
    }

    public String getEmail() {
        return email;
    }

    public int getId() {
        return id;
    }
}
